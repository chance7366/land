function allGroups(overrides: Partial<FieldSpec> & Pick<FieldSpec, "field_name" | "label" | "data_type" | "step" | "storage">): FieldSpec {
  return {
    is_required: false,
    validation_rule: "",
    naver_sync_note: "",
    category_groups: [...ALL_CATEGORY_GROUPS],
    ...overrides,
  };
}

function forGroups(
  groups: CategoryGroup[],
  overrides: Partial<FieldSpec> & Pick<FieldSpec, "field_name" | "label" | "data_type" | "step" | "storage">,
): FieldSpec {
  return {
    is_required: false,
    validation_rule: "",
    naver_sync_note: "",
    category_groups: groups,
    ...overrides,
  };
}

