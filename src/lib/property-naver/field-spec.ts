export function getFieldsForGroup(group: CategoryGroup, step?: FormStep): FieldSpec[] {
  return ALL_FIELD_SPECS.filter(
    (f) => f.category_groups.includes(group) && (step == null || f.step === step),
  );
}

export function getFieldsForStep(group: CategoryGroup, step: FormStep): FieldSpec[] {
  return getFieldsForGroup(group, step);
}

/** Deduplicate by field_name keeping first (column fields shared across groups). */
export function uniqueFields(fields: FieldSpec[]): FieldSpec[] {
  const seen = new Set<string>();
  return fields.filter((f) => {
    if (seen.has(f.field_name)) return false;
    seen.add(f.field_name);
    return true;
  });
}
